#include <stdio.h>
#include <stdlib.h>

/**
   * Operadores matemáticos:
   * Soma -> +
   * Subtração -> -
   * Multiplicação -> *
   * Divisão -> /
*/

int main() {

  float num1 = 10.0;
  float num2 = 20.0;

  printf("\nSoma: %.1f\n", num1 + num2);
  printf("\nSubtracao: %.1f\n", num1 - num2);
  printf("\nMultiplicacao: %.1f\n", num1 * num2);
  printf("\nDivisao: %.1f\n", num1 / num2);
}