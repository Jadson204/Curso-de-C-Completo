#include <stdio.h>
#include <stdlib.h>

/**
   * Operadores matemáticos:
   * Soma -> +
   * Subtração -> -
   * Multiplicação -> *
   * Divisão -> /
*/

int main() {

  int num1 = 10;
  int num2 = 20;

  printf("\nSoma: %d\n", num1 + num2);
  printf("\nSubtracao: %d\n", num1 - num2);
  printf("\nMultiplicacao: %d\n", num1 * num2);
  printf("\nDivisao: %f\n", (float)num1 / num2);
}